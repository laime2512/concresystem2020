package Modelos;

public class Posicion {
	private Integer codposicion;
	private String nombre;
	private Integer estado;
	private Integer codmueble;
	public Integer getCodposicion() {
		return codposicion;
	}
	public void setCodposicion(Integer codposicion) {
		this.codposicion = codposicion;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
	public Integer getCodmueble() {
		return codmueble;
	}
	public void setCodmueble(Integer codmueble) {
		this.codmueble = codmueble;
	}
}
