package Modelos;

public class Mueble {
	private Integer codmueble;
	private String nombre;
	private Integer estado;
	private Integer codtimu;
	public Integer getCodmueble() {
		return codmueble;
	}
	public void setCodmueble(Integer codmueble) {
		this.codmueble = codmueble;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
	public Integer getCodtimu() {
		return codtimu;
	}
	public void setCodtimu(Integer codtimu) {
		this.codtimu = codtimu;
	}
}
