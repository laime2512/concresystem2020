package Modelos;

public class Tipo {
	private Integer codtip;
	private String nomtip;
	private Integer estado;
	private Integer codcat;
	public Integer getCodtip() {
		return codtip;
	}
	public void setCodtip(Integer codtip) {
		this.codtip = codtip;
	}
	public String getNomtip() {
		return nomtip;
	}
	public void setNomtip(String nomtip) {
		this.nomtip = nomtip;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
	public Integer getCodcat() {
		return codcat;
	}
	public void setCodcat(Integer codcat) {
		this.codcat = codcat;
	}
	
}
